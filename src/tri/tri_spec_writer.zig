pub const SpecField = struct {
    name: []const u8,
    field_type: []const u8,
};

pub const SpecType = struct {
    name: []const u8,
    fields: []const SpecField,
};

pub const SpecBehavior = struct {
    name: []const u8,
    description: []const u8,
    inputs: []const SpecField,
    output: []const u8,
    steps: []const []const u8,
};

pub const SpecTemplate = struct {
    module_name: []const u8,
    description: []const u8,
    types: []const SpecType,
    behaviors: []const SpecBehavior,
};

pub const WriteResult = struct {
    spec_path: []const u8,
    generated_path: []const u8,
    compile_ok: bool,
    error_msg: ?[]const u8,
};

