test "extractModuleName — conventional commit" {
    const allocator = std.testing.allocator;
    const name = try extractModuleName(allocator, "feat(mu): Agent MU Error Protocol");
    defer allocator.free(name);
    try std.testing.expectEqualStrings("agent_m_u_error_protocol", name);
}

