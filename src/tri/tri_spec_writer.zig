/// Extract module name from issue title.
/// "feat(mu): Agent MU Error Protocol" → "agent_mu_error_protocol"
pub fn extractModuleName(allocator: Allocator, title: []const u8) ![]u8 {
    // Skip conventional commit prefix: "feat(scope): " or "fix(scope): "
    var start: usize = 0;
    if (std.mem.indexOf(u8, title, ": ")) |colon_pos| {
        start = colon_pos + 2;
    }

    // Convert remaining to snake_case
    var result: std.ArrayList(u8) = .empty;
    errdefer result.deinit(allocator);

    var prev_was_sep = false;
    for (title[start..]) |c| {
        if (c == ' ' or c == '-' or c == '/' or c == '(' or c == ')' or c == '[' or c == ']') {
            if (!prev_was_sep and result.items.len > 0) {
                try result.append(allocator, '_');
                prev_was_sep = true;
            }
        } else if (c >= 'A' and c <= 'Z') {
            if (result.items.len > 0 and !prev_was_sep) {
                try result.append(allocator, '_');
            }
            try result.append(allocator, c + 32); // toLower
            prev_was_sep = false;
        } else if ((c >= 'a' and c <= 'z') or (c >= '0' and c <= '9') or c == '_') {
            try result.append(allocator, c);
            prev_was_sep = false;
        }
    }

    // Trim trailing underscore
    while (result.items.len > 0 and result.items[result.items.len - 1] == '_') {
        _ = result.pop();
    }

    return result.toOwnedSlice(allocator);
}

