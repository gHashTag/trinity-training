/// Generate a .tri spec file content from a template.
pub fn generateSpecContent(allocator: Allocator, template: SpecTemplate) ![]u8 {
    var buf: std.ArrayList(u8) = .empty;
    errdefer buf.deinit(allocator);
    const w = buf.writer(allocator);

    // Header
    try w.writeAll("# ═══════════════════════════════════════════════════════════════════════════════\n");
    try w.print("# VIBEE Specification — {s}\n", .{template.module_name});
    try w.writeAll("# ═══════════════════════════════════════════════════════════════════════════════\n");
    try w.writeAll("# φ² + 1/φ² = 3 = TRINITY\n");
    try w.writeAll("# Auto-generated by Spec Writer Pipeline (Issue #76)\n");
    try w.writeAll("# ═══════════════════════════════════════════════════════════════════════════════\n\n");

    // Metadata
    try w.print("name: {s}\n", .{template.module_name});
    try w.writeAll("version: \"1.0.0\"\n");
    try w.writeAll("language: zig\n");
    try w.print("module: {s}\n\n", .{template.module_name});

    // Description
    try w.writeAll("description: |\n");
    // Indent each line of description
    var desc_iter = std.mem.splitSequence(u8, template.description, "\n");
    while (desc_iter.next()) |line| {
        try w.print("  {s}\n", .{line});
    }
    try w.writeByte('\n');

    // Types
    if (template.types.len > 0) {
        try w.writeAll("types:\n");
        for (template.types) |t| {
            try w.print("  {s}:\n", .{t.name});
            try w.writeAll("    fields:\n");
            for (t.fields) |f| {
                try w.print("      {s}: {s}\n", .{ f.name, f.field_type });
            }
        }
        try w.writeByte('\n');
    }

    // Behaviors
    if (template.behaviors.len > 0) {
        try w.writeAll("behaviors:\n");
        for (template.behaviors) |b| {
            try w.print("  {s}:\n", .{b.name});
            try w.print("    description: \"{s}\"\n", .{b.description});
            if (b.inputs.len > 0) {
                try w.writeAll("    inputs:\n");
                for (b.inputs) |inp| {
                    try w.print("      - {s}: {s}\n", .{ inp.name, inp.field_type });
                }
            }
            try w.print("    output: {s}\n", .{b.output});
            if (b.steps.len > 0) {
                try w.writeAll("    steps:\n");
                for (b.steps) |step| {
                    try w.print("      - {s}\n", .{step});
                }
            }
        }
    }

    return buf.toOwnedSlice(allocator);
}

/// Write spec to file and run vibee gen + ast-check.
pub fn writeAndValidate(allocator: Allocator, template: SpecTemplate) !WriteResult {
    const content = try generateSpecContent(allocator, template);
    defer allocator.free(content);

    // Build paths
    const spec_path = try std.fmt.allocPrint(allocator, "specs/tri/{s}.tri", .{template.module_name});
    const gen_path = try std.fmt.allocPrint(allocator, "generated/{s}.zig", .{template.module_name});

    // Write spec file
    const spec_file = try std.fs.cwd().createFile(spec_path, .{});
    defer spec_file.close();
    try spec_file.writeAll(content);

    // Run vibee gen
    const gen_result = std.process.Child.run(.{
        .allocator = allocator,
        .argv = &.{ "zig", "build", "vibee", "--", "gen", spec_path },
    }) catch {
        return .{
            .spec_path = spec_path,
            .generated_path = gen_path,
            .compile_ok = false,
            .error_msg = "vibee gen failed to execute",
        };
    };
    defer allocator.free(gen_result.stdout);
    defer allocator.free(gen_result.stderr);

    const gen_exit = switch (gen_result.term) {
        .Exited => |code| code,
        else => @as(u32, 1),
    };
    if (gen_exit != 0) {
        return .{
            .spec_path = spec_path,
            .generated_path = gen_path,
            .compile_ok = false,
            .error_msg = "vibee gen returned non-zero exit code",
        };
    }

    // Run zig ast-check
    const ast_result = std.process.Child.run(.{
        .allocator = allocator,
        .argv = &.{ "zig", "ast-check", gen_path },
    }) catch {
        return .{
            .spec_path = spec_path,
            .generated_path = gen_path,
            .compile_ok = false,
            .error_msg = "zig ast-check failed to execute",
        };
    };
    defer allocator.free(ast_result.stdout);
    defer allocator.free(ast_result.stderr);

    const ast_exit = switch (ast_result.term) {
        .Exited => |code| code,
        else => @as(u32, 1),
    };
    return .{
        .spec_path = spec_path,
        .generated_path = gen_path,
        .compile_ok = ast_exit == 0,
        .error_msg = if (ast_exit != 0) "ast-check failed" else null,
    };
}

