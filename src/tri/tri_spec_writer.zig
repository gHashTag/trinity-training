test "generateSpecContent — basic template" {
    const allocator = std.testing.allocator;

    const template = SpecTemplate{
        .module_name = "test_module",
        .description = "A test module for validation.",
        .types = &.{
            .{
                .name = "Config",
                .fields = &.{
                    .{ .name = "enabled", .field_type = "Bool" },
                    .{ .name = "max_retries", .field_type = "Int" },
                },
            },
        },
        .behaviors = &.{
            .{
                .name = "init",
                .description = "Initialize the module",
                .inputs = &.{},
                .output = "Config",
                .steps = &.{ "Create default config", "Return config" },
            },
        },
    };

    const content = try generateSpecContent(allocator, template);
    defer allocator.free(content);

    // Verify key parts exist
    try std.testing.expect(std.mem.indexOf(u8, content, "name: test_module") != null);
    try std.testing.expect(std.mem.indexOf(u8, content, "language: zig") != null);
    try std.testing.expect(std.mem.indexOf(u8, content, "Config:") != null);
    try std.testing.expect(std.mem.indexOf(u8, content, "enabled: Bool") != null);
    try std.testing.expect(std.mem.indexOf(u8, content, "init:") != null);
}
